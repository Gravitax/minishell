#include "libft.h"

int			ft_isprime(int nb)
{
	int x;

	x = 3;
	if (nb <= 3)
		return (nb <= 1 ? 0 : 1);
	if (nb % 2 == 0 || nb % 3 == 0)
		return (0);
	while (x * x <= nb)
	{
		if (nb % x == 0)
			return (0);
		x += 2;
	}
	return (1);
}
