#include "libft.h"

void		ft_print_hex(int nb)
{
	if (nb >= 16)
		ft_print_hex(nb / 16);
	nb = nb % 16;
	nb += nb < 10 ? '0' : 'a' - 10;
	write(1, &nb, 1);
}
