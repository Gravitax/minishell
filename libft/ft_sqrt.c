#include "libft.h"

float		ft_sqrt(int n)
{
	float	nb;
	float	x;
	int		i;

	nb = (float)n;
	x = 1;
	i = 23;
	while (--i)
		x = (0.5) * (x + nb / x);
	return (x);
}
